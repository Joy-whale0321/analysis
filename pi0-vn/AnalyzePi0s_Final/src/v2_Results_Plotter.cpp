void initializeEnvironment() {
    gROOT->LoadMacro("sPhenixStyle.C");
    SetsPhenixStyle();
}
void v2_Results_Plotter() {
    initializeEnvironment();
    
    Data data1;
    
    // Read the datasets
    Read_DataSet(ReferenceData, data1);
    
    ReadStatUncertainties(statUncertaintyFilePath, data1);

    
    plotting_Results_andPHENIXoverlay(data1);
    plotting_Systematics(data1);
    
    EMCal_Systematic_Data EMCal_Systematic_data1;
    ReadStatUncertainties_emCalScaleCSV(statUncertaintyFilePath_EMCal_Contributors, EMCal_Systematic_data1);
    plotting_EMCal_Syst_Contributors(EMCal_Systematic_data1);
    
    PlotConfig config = initializePlotConfig();

    Data data_SYST1CEMC, data_SYST2CEMC, data_SYST3DCEMC, data_SYST3UCEMC, data_SYST4CEMC, dataP013, dataAsymLow, dataAsymHigh, data_Signal_Bound_Variation, data_SampleSizeVariation;

    if (config.plotSignalWindowVariations) {
        Read_DataSet(filePathSignal_Bound_Variation, data_Signal_Bound_Variation);
        plot_SignalWindow_Variation(data1, data_Signal_Bound_Variation);
    }
    if (config.plotEMCalScaleVariations) {
        Read_DataSet(filePathEMCal_Syst_SYST1CEMC, data_SYST1CEMC);
        Read_DataSet(filePathEMCal_Syst_SYST2CEMC, data_SYST2CEMC);
        Read_DataSet(filePathEMCal_Syst_SYST3DCEMC, data_SYST3DCEMC);
        Read_DataSet(filePathEMCal_Syst_SYST3UCEMC, data_SYST3UCEMC);
        Read_DataSet(filePathEMCal_Syst_SYST4CEMC, data_SYST4CEMC);
        plot_EMCal_Scale_v2_overlays(data1, data_SYST1CEMC, data_SYST2CEMC, data_SYST3DCEMC, data_SYST3UCEMC, data_SYST4CEMC);
    }
    if (config.plotAsymmetryCutVariations) {
        Read_DataSet(filePath_AsymmetryVariations_45, dataAsymLow);
        Read_DataSet(filePath_AsymmetryVariations_55, dataAsymHigh);

        plot_AsymmetryCut_Variations(data1, dataAsymLow, dataAsymHigh);
    }
    if (config.plotSampleSizeVariations) {
        Read_DataSet(filePathSampleSizeVariation, data_SampleSizeVariation);
        plot_SampleSize_Variations(data1, data_SampleSizeVariation);
    }
    if (config.plotSigBgCorrAnd_bgWindowVariations) {
        plot_sigBgCorr_and_bgWindowVariations(data1);
    }
    if (config.plotProduction_Comparisons) {
        Read_DataSet(p013_filePath, dataP013);
        plotProduction_Comparisons(data1, dataP013);
    }
}
